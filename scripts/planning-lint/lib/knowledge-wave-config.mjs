/** Phase 3 knowledge wave targets and themes (wave 61+ formulas). */

export function waveTargets(wave) {
  const n = wave - 61;
  return {
    obligations: 120 + n * 10,
    protocols: 600 + n * 50,
    retrieval: 90 + n * 10,
    imagination: 1050 + n * 50,
    pc: wave < 63 ? 0 : 20 + (wave - 63) * 10,
    adversarial: 10 + Math.max(0, wave - 68) * 2,
    edgeEm4: 110 + Math.max(0, wave - 68) * 5,
    em1Links: 12,
    microCards: 10,
  };
}


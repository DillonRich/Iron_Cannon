/** EM-0 config + flow_step node catalog — 205 config nodes + key-flow steps */

function cfg({
  host,
  adapterIds,
  setting,
  modules = [],
  flows = [],
  userFlowEffect,
  securityEffects = [],
  legalEffects = [],
  referenceRefIds = [],
  t02Missing = false,
}) {
  const nodeId = `cfg/${host}/${setting.replace(/[^a-zA-Z0-9_]+/g, '_')}`;
  return {
    nodeId,
    type: 'config',
    host,
    adapterIds,
    setting,
    requiredForModules: modules,
    requiredForFlows: flows,
    userFlowEffect,
    securityEffects,
    legalEffects,
    detectSignals: { envExample: setting, t02Missing },
    referenceRefIds,
  };
}

function step({
  nodeId,
  host,
  route,
  phase,
  moduleIds,
  flows = [],
  securityEffects = [],
  legalEffects = [],
  agentGuidance,
  referenceRefIds = [],
}) {
  return {
    nodeId,
    type: 'flow_step',
    host,
    route,
    phase,
    moduleIds,
    requiredForFlows: flows,
    securityEffects,
    legalEffects,
    agentGuidance,
    referenceRefIds,
  };
}

